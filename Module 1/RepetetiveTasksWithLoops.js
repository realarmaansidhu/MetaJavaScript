var n = 5;
for(var i=0; i<n; i++) {
    console.log(i+1);
}
console.log("Counting Completed");

var i = 0;
while(i < 5) {
    console.log(5-i);
    i++;
}
console.log("Counting Completed");

var i = 5;
while(i--) {
    console.log(i+1);
}
console.log("Countdown Finished");

